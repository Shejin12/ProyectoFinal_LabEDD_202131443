    private void actualzarAdyacentes(){
        Nodo start = grafo.findNodo(inicio);
        if (start != null) {
            LinkedList<Nodo> juntos = start.getAdyacentes();
            for (Nodo junto : juntos) {
                cmbSiguiente.addItem(junto.getNombre());
            }
        }
    }
